package vidhya;

public class DataTypes {
		public static void main(String args[]){
			int intNum=10;
			System.out.println("the integer num is :"+intNum);
			float floatNum=20.109988f;
			System.out.println("the float num is :" +floatNum);
			long longNum=1225415154;
			System.out.println("the long num is :" +longNum);
			double decimal=1.21111111111111111111113;
			System.out.println("the decimal num is :" +decimal);
			short shortNum=100;
			System.out.println("the short num is :" +shortNum);		
			char character='A';
			System.out.println("the character is :" +character);
			boolean flag=false;
			System.out.println("the boolean value is : " +flag);
			byte byteNum=-110;
			System.out.println("the byte value is :" +byteNum);
		}
}
