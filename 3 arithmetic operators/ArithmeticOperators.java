class ArithmeticOperators{
	public static void main(String args[]){
		System.out.println("The Addition of Two Variables");
		int num1=8;
		int num2=4;
		int sum,sub,div,mod;
		sum=num1+num2;
		System.out.println("the sum of two numbers 8 and 2 is: "+sum);
		sub=num1-num2;
		System.out.println("the difference b/w two numbers 8 and 2 is: " +sub);
		div=num1/num2;
		System.out.println("the divison of two numbers 8 and 2 is: " +div);
		int n1=11,n2=2;
		mod=n1%n2;
		System.out.println("the modulus of two numbers 11 and 2 is: " +mod);
		double mul;
		double dn1=12.13,dn2=2.36;
		mul=dn1*dn2;
		System.out.println("the multiplication of two numbers 12.13 and 2.36 is: " +mul);

	}
}