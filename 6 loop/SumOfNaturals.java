package vidhya;

public class SumOfNaturals {
	 public static void main(String[] args) {

	        int sum = 0;

	        for(int i = 5; i <= 25; ++i)
	        {
	            // sum = sum + i;
	            sum += i;
	        }

	        System.out.println("Sum = " + sum);
	    }
	}
			