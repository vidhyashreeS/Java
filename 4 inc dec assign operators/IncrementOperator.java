class IncrementOperator{
	public static void main(String args[]){
		System.out.println("Unary IncrementOperator");
		int num=6;
		System.out.println("post Increment number is: "+num);
		System.out.println("the num after post increment is "+num++);
		System.out.println("the num after post increment is "+num++);
		int num1=22;
		System.out.println("pre Increment number is: "+num1);
		System.out.println("the num after pre increment is "+ ++num1);
		System.out.println("the num again after pre increment is "+ ++num1);
		}
}