class AssignmentOperator{
	public static void main(String args[])
	{
		System.out.println(" --------AssignmentOperator--------- ");
		{
			int num;
			num=10;
			System.out.println("the given number is 10");
			num+=5;
			System.out.println("the result of num+=5 is: " +num);
		}
		{
			int n1;
			n1=10;
			System.out.println("the given number is 10");
			n1-=5;
			System.out.println("the result of n1-=5 is: " +n1);
		}
		{
			int n2;
			n2=10;
			System.out.println("the given number is 10");
			n2*=5;
			System.out.println("the result of n2*=5 is: " +n2);
		}
		{
			int n3;
			n3=10;
			System.out.println("the given number is 10");
			n3/=5;
			System.out.println("the result of n3/=5 is: " +n3);
		}
		{
			int n4;
			n4=10;
			System.out.println("the given number is 10");
			n4%=3;
			System.out.println("the result of n4%=3 is: " +n4);
		}
	}
}
		